// Get a random quote based on the day (so it stays consistent throughout the day)
export function getDailyQuote(date: Date = new Date()): typeof TRADING_QUOTES[number] {
  // Use the date string as a seed for consistency throughout the day
  const dateString = date.toISOString().split('T')[0];
  const seed = dateString.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const index = seed % TRADING_QUOTES.length;
  return TRADING_QUOTES[index];
}

// Get a completely random quote
export function getRandomQuote(): typeof TRADING_QUOTES[number] {
  const index = Math.floor(Math.random() * TRADING_QUOTES.length);
  return TRADING_QUOTES[index];
}
